    private void jButtonRegistratiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistratiActionPerformed
        String email = jTextFieldEmail.getText();
        String password = new String(jPasswordField.getPassword());

        new AnagraficaManager().login(email, password, new Listener<Pair<Integer, Anagrafica>>() {
            @Override
            public void result(Pair<Integer, Anagrafica> obj) {
                switch (obj.getKey()) {
                    case StatusCodes.SUCCESS:
                        JOptionPane.showMessageDialog(rootPane, String.format("Benvenuto %s %s", obj.getValue().getNome(), obj.getValue().getCognome()));
                        break;
                    case StatusCodes.NOT_FOUND:
                        JOptionPane.showMessageDialog(rootPane, "Username o password errata.", "Attenzione!", JOptionPane.ERROR_MESSAGE);
                        break;
                    default:
                        JOptionPane.showMessageDialog(rootPane, "Si Ã¨ verificato un errore nel server. Riprova o contatta gli sviluppatori.", "Attenzione", JOptionPane.ERROR_MESSAGE);
                        break;
                }
            }
        });
    }//GEN-LAST:event_jButtonRegistratiActionPerformed

